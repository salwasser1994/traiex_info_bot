# –û–±—Ä–∞–±–æ—Ç–∫–∞ inline-–∫–Ω–æ–ø–æ–∫
@dp.callback_query()
async def callbacks(callback: types.CallbackQuery):
    data = callback.data
    idx_str, opt_str = data.split("_")
    idx = int(idx_str)
    opt = int(opt_str)
    q = test_questions[idx]
    selected_option = q["options"][opt]

    if selected_option == q["correct"]:
        await callback.message.answer("‚úÖ –ü—Ä–∞–≤–∏–ª—å–Ω–æ!")
        idx += 1
        if idx < len(test_questions):
            user_progress[callback.from_user.id] = idx
            await send_test_question(callback.message, idx)
        else:
            await callback.message.answer("üéâ –¢–µ—Å—Ç –∑–∞–≤–µ—Ä—à—ë–Ω!", reply_markup=main_menu())
            user_progress.pop(callback.from_user.id, None)
    else:
        await callback.message.answer("‚ùå –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.")

    await callback.answer()

