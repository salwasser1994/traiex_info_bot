# –û–±—Ä–∞–±–æ—Ç–∫–∞ inline-–∫–Ω–æ–ø–æ–∫ –¥–ª—è —Ç–µ—Å—Ç–∞
@dp.callback_query()
async def handle_callback(callback: types.CallbackQuery):
    user_id = callback.from_user.id
    if user_id not in user_progress:
        await callback.answer()
        return
    idx = user_progress[user_id]
    q = test_questions[idx]
    choice = int(callback.data.split("_")[1])
    selected = q["options"][choice]

    if selected == q["correct"]:
        await callback.message.answer("‚úÖ –ü—Ä–∞–≤–∏–ª—å–Ω–æ!")
        idx += 1
        if idx < len(test_questions):
            user_progress[user_id] = idx
            await send_test_question(callback.message, idx)
        else:
            await callback.message.answer("üéâ –¢–µ—Å—Ç –∑–∞–≤–µ—Ä—à—ë–Ω!", reply_markup=main_menu())
            del user_progress[user_id]
    # –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç ‚Äî –º–æ–ª—á–∏–º
    await callback.answer()

