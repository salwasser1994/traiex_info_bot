# –û–±—Ä–∞–±–æ—Ç–∫–∞ inline-–∫–Ω–æ–ø–æ–∫ —Ç–µ—Å—Ç–∞
@dp.callback_query()
async def handle_test_answer(callback: types.CallbackQuery):
    user_id = callback.from_user.id
    idx_str, answer = callback.data.split("|")
    idx = int(idx_str)
    correct = test_questions[idx]["correct"]
    
    if answer == correct:
        await callback.message.answer("‚úÖ –ü—Ä–∞–≤–∏–ª—å–Ω–æ!")
        idx += 1
        if idx < len(test_questions):
            user_progress[user_id] = idx
            await send_test_question(callback.message, idx)
        else:
            await callback.message.answer("üéâ –¢–µ—Å—Ç –∑–∞–≤–µ—Ä—à—ë–Ω!", reply_markup=main_menu())
            del user_progress[user_id]
    else:
        await callback.message.answer("‚ùå –ù–µ–≤–µ—Ä–Ω–æ. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–ª–µ–¥—É—é—â–∏–π –≤–æ–ø—Ä–æ—Å.")
        idx += 1
        if idx < len(test_questions):
            user_progress[user_id] = idx
            await send_test_question(callback.message, idx)
        else:
            await callback.message.answer("üéâ –¢–µ—Å—Ç –∑–∞–≤–µ—Ä—à—ë–Ω!", reply_markup=main_menu())
            del user_progress[user_id]
    await callback.answer()

